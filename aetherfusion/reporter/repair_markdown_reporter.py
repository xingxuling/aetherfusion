"""Markdown reporter for repair plans.

Generates a human-readable repair plan report with sections for
summary, failed commands, error classification, repair items,
automation eligibility, blocked actions, and next command.
"""

from typing import Any


def generate_repair_report(plan: dict[str, Any]) -> str:
    """Generate a Markdown repair plan report from a repair plan dict.

    Args:
        plan: Repair plan dict from generate_repair_plan.

    Returns:
        Markdown string for the report.
    """
    lines: list[str] = []

    version = plan.get("repair_plan_version", "?")
    source = plan.get("source_verify_file", "?")
    summary = plan.get("summary", {})
    failed_cmds = plan.get("failed_commands", [])
    classified = plan.get("classified_errors", {})
    items = plan.get("repair_items", [])
    blocked = plan.get("blocked_actions", [])
    next_cmd = plan.get("next_recommended_command", "")

    # -- Title ----------------------------------------------------------------
    lines.append("# AetherFusion Repair Plan")
    lines.append("")
    lines.append(f"> **Generated by AetherFusion v{version}**")
    lines.append(f"> Source verify file: `{source}`")
    lines.append("")
    lines.append("> **v0.6 Repair Plan is read-only.** No source or target files were modified.")
    lines.append("")

    # -- 1. Summary -----------------------------------------------------------
    lines.append("## 1. Summary")
    lines.append("")
    total = summary.get("total_failed", 0)
    classified_count = summary.get("classified_count", 0)
    manual = summary.get("manual_only", 0)
    plan_only = summary.get("plan_only", 0)
    safe_auto = summary.get("safe_auto_candidate", 0)

    lines.append(f"| Metric | Count |")
    lines.append(f"|--------|-------|")
    lines.append(f"| Total failed commands | {total} |")
    lines.append(f"| Classified errors | {classified_count} |")
    lines.append(f"| Manual-only items | {manual} |")
    lines.append(f"| Plan-only items | {plan_only} |")
    lines.append(f"| Safe auto candidates | {safe_auto} |")
    lines.append("")

    # -- 2. Failed Commands --------------------------------------------------
    lines.append("## 2. Failed Commands")
    lines.append("")
    if failed_cmds:
        for cmd in failed_cmds:
            lines.append(f"- `{cmd}`")
    else:
        lines.append("No failed commands — verify result may have passed entirely.")
    lines.append("")

    # -- 3. Error Classification ---------------------------------------------
    lines.append("## 3. Error Classification")
    lines.append("")
    if classified:
        lines.append(f"| Error Type | Count |")
        lines.append(f"|------------|-------|")
        for et, count in sorted(classified.items(), key=lambda x: -x[1]):
            lines.append(f"| {et} | {count} |")
    else:
        lines.append("No errors to classify.")
    lines.append("")

    # -- 4. Repair Items -----------------------------------------------------
    lines.append("## 4. Repair Items")
    lines.append("")
    if items:
        for i, item in enumerate(items, 1):
            cmd = item.get("command", "?")
            error_type = item.get("error_type", "?")
            severity = item.get("severity", "?")
            confidence = item.get("confidence", 0)
            risk = item.get("risk_level", "?")
            auto = item.get("automation_eligibility", "?")
            action = item.get("recommended_action", "")
            evidence = item.get("evidence", "")
            suspected = item.get("suspected_files", [])

            lines.append(f"### 4.{i}. `{cmd}`")
            lines.append("")
            lines.append(f"- **Error type:** {error_type}")
            lines.append(f"- **Severity:** {severity} | **Confidence:** {confidence}% | **Risk:** {risk}")
            lines.append(f"- **Automation:** {auto}")

            if suspected:
                lines.append(f"- **Suspected files:** {', '.join(suspected)}")

            lines.append(f"- **Recommendation:** {action}")

            if evidence:
                lines.append("")
                lines.append("**Evidence:**")
                lines.append("```")
                lines.append(evidence[:800])
                lines.append("```")

            lines.append("")
    else:
        lines.append("No repair items to show.")
        lines.append("")

    # -- 5. Automation Eligibility -------------------------------------------
    lines.append("## 5. Automation Eligibility")
    lines.append("")
    lines.append(f"- **Manual-only:** {manual} items require full human intervention")
    lines.append(f"- **Plan-only:** {plan_only} items can be planned but not auto-executed")
    lines.append(f"- **Safe auto candidate:** {safe_auto} items are candidates for future automated repair")
    lines.append("")

    # -- 6. Blocked Actions --------------------------------------------------
    lines.append("## 6. Blocked Actions")
    lines.append("")
    lines.append("The following actions are **blocked** in v0.6:")
    lines.append("")
    for ba in blocked:
        lines.append(f"- {ba}")
    lines.append("")

    # -- 7. Next Recommended Command -----------------------------------------
    lines.append("## 7. Next Recommended Command")
    lines.append("")
    lines.append("```bash")
    lines.append(next_cmd)
    lines.append("```")
    lines.append("")

    return "\n".join(lines)