def _run_scan(args: argparse.Namespace) -> None:
    """Execute the scan command.

    Supports:
    - --out:  Markdown human-readable report
    - --json: machine-readable JSON map for downstream agent consumption
    At least one of --out or --json must be provided.
    """
    source_path = Path(args.source).resolve()
    target_path = Path(args.target).resolve()

    # Validate inputs
    if not source_path.is_dir():
        print(f"Error: Source path is not a directory: {source_path}", file=sys.stderr)
        sys.exit(1)
    if not target_path.is_dir():
        print(f"Error: Target path is not a directory: {target_path}", file=sys.stderr)
        sys.exit(1)

    # Require at least one output
    if args.out is None and args.json_out is None:
        print("Error: No output specified. Provide --out and/or --json.", file=sys.stderr)
        sys.exit(1)

    # --- Git status checks (read-only) ---
    source_git = check_git_status(source_path)
    target_git = check_git_status(target_path)
    _print_git_status("source", source_git)
    _print_git_status("target", target_git)

    # --- Scan ---
    print(f"Scanning source: {source_path}")
    source_info = ProjectAnalyzer(source_path).analyze()
    print(f"  -> {source_info.file_count} files, {source_info.dir_count} dirs, "
          f"{len(source_info.config_files_detected)} configs")

    print(f"Scanning target: {target_path}")
    target_info = ProjectAnalyzer(target_path).analyze()
    print(f"  -> {target_info.file_count} files, {target_info.dir_count} dirs, "
          f"{len(target_info.config_files_detected)} configs")

    # --- Markdown report ---
    if args.out:
        out_path = Path(args.out).resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        print(f"Generating Markdown report...")
        report = generate_report(source_info, target_info)
        out_path.write_text(report, encoding="utf-8")
        print(f"  -> {out_path}")

    # --- JSON map ---
    if args.json_out:
        json_path = Path(args.json_out).resolve()
        print(f"Generating JSON map...")
        data = generate_json_map(source_info, target_info)
        write_json_map(json_path, data)
        print(f"  -> {json_path}")

    print("Done.")


def _print_git_status(label: str, git_result: dict) -> None:
    """Print a concise git status line for the CLI."""
    status = git_result["status"]
    branch = git_result.get("branch")
    changed = len(git_result.get("changed_files", []))

    if status == "not_git_repo":
        print(f"Git [{label}]: not a git repository")
    elif status == "clean":
        print(f"Git [{label}]: clean (branch: {branch})")
    elif status == "dirty":
        print(f"Git [{label}]: dirty — {changed} changed file(s) (branch: {branch})")
    else:
        msg = git_result.get("error_message", "unknown error")
        print(f"Git [{label}]: error — {msg}")


def _run_plan(args: argparse.Namespace) -> None:
    """Execute the plan command.

    Loads a JSON project map and generates a module-level fusion plan.
    At least one of --out or --json must be specified.
    Exits with code 1 if the module is not found or the map is invalid.
    """
    map_path = Path(args.map).resolve()

    if not map_path.is_file():
        print(f"Error: JSON map not found: {map_path}", file=sys.stderr)
        sys.exit(1)

    if args.out is None and args.json_out is None:
        print("Error: No output specified. Provide --out and/or --json.", file=sys.stderr)
        sys.exit(1)

    print(f"Loading project map: {map_path}")
    try:
        plan = generate_fusion_plan(map_path, args.module)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in map file: {e}", file=sys.stderr)
        sys.exit(1)
    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    module_name = plan.get("module_name", "?")
    risk_level = plan.get("risk_level", "?")
    strategy = plan.get("strategy", "?")

    print(f"  -> Module: {module_name}")
    print(f"  -> Risk: {risk_level}, Strategy: {strategy}")

    # Markdown plan
    if args.out:
        out_path = Path(args.out).resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        report = generate_plan_report(plan)
        out_path.write_text(report, encoding="utf-8")
        print(f"  -> Markdown plan: {out_path}")

    # JSON plan
    if args.json_out:
        json_path = Path(args.json_out).resolve()
        write_plan_json(json_path, plan)
        print(f"  -> JSON plan: {json_path}")

    print("Done.")


def _run_patch(args: argparse.Namespace) -> None:
    """Execute the patch command.

    Generates a dry-run patch preview from a fusion plan JSON.
    Requires --dry-run; refuses to run otherwise.
    At least one of --out, --json, or --diff must be specified.
    """
    # Enforce --dry-run
    if not args.dry_run:
        print(
            "Error: The 'patch' command requires --dry-run. "
            "v0.3 only supports dry-run mode; no files are modified.",
            file=sys.stderr,
        )
        sys.exit(1)

    if args.out is None and args.json_out is None and args.diff_out is None:
        print(
            "Error: No output specified. Provide --out, --json, and/or --diff.",
            file=sys.stderr,
        )
        sys.exit(1)

    plan_path = Path(args.plan).resolve()

    if not plan_path.is_file():
        print(f"Error: Fusion plan not found: {plan_path}", file=sys.stderr)
        sys.exit(1)

    print(f"Loading fusion plan: {plan_path}")

    try:
        manifest = generate_dry_run_patch(plan_path)
    except (FileNotFoundError, ValueError) as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in plan file: {e}", file=sys.stderr)
        sys.exit(1)

    summary = manifest.get("summary", {})
    print(f"  -> Module: {manifest.get('module_name', '?')}")
    print(f"  -> Files to add: {summary.get('files_to_add', 0)}")
    print(f"  -> Files conflicted: {summary.get('files_conflicted', 0)}")
    print(f"  -> Files skipped: {summary.get('files_skipped', 0)}")
    print(f"  -> Mode: {manifest.get('mode', '?')}")

    # Markdown patch preview
    if args.out:
        out_path = Path(args.out).resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        report = generate_patch_report(manifest)
        out_path.write_text(report, encoding="utf-8")
        print(f"  -> Markdown preview: {out_path}")

    # JSON patch manifest
    if args.json_out:
        json_path = Path(args.json_out).resolve()
        write_patch_json(json_path, manifest)
        print(f"  -> JSON manifest: {json_path}")

    # Diff output
    if args.diff_out:
        diff_path = Path(args.diff_out).resolve()
        generate_diff(manifest, diff_path)
        print(f"  -> Diff file: {diff_path}")

    print("Done. No source or target files were modified.")


def _run_apply(args: argparse.Namespace) -> None:
    """Execute the apply command.

    Applies only add_file operations from a patch manifest.
    Requires --confirm; refuses to run otherwise.
    At least one of --out or --json must be specified.
    Writes a rollback manifest if --backup is provided.
    """
    # Enforce --confirm
    if not args.confirm:
        print(
            "Error: The 'apply' command requires --confirm. "
            "This ensures you have explicitly acknowledged the intent to modify "
            "the target project by copying new files into it.",
            file=sys.stderr,
        )
        sys.exit(1)

    if args.out is None and args.json_out is None:
        print(
            "Error: No output specified. Provide --out and/or --json.",
            file=sys.stderr,
        )
        sys.exit(1)

    patch_path = Path(args.patch).resolve()

    if not patch_path.is_file():
        print(f"Error: Patch manifest not found: {patch_path}", file=sys.stderr)
        sys.exit(1)

    print(f"Applying patch manifest: {patch_path}")

    backup_path = Path(args.backup_path).resolve() if args.backup_path else None

    try:
        result = apply_patch(patch_path, backup_path)
    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in patch manifest: {e}", file=sys.stderr)
        sys.exit(1)

    summary = result.get("summary", {})
    print(f"  -> Module: {result.get('module_name', '?')}")
    print(f"  -> Mode: {result.get('mode', '?')}")
    print(f"  -> Applied: {summary.get('files_applied', 0)}")
    print(f"  -> Skipped: {summary.get('files_skipped', 0)}")
    print(f"  -> Blocked: {summary.get('files_blocked', 0)}")
    print(f"  -> Failed:  {summary.get('files_failed', 0)}")
    if result.get("rollback_manifest_path"):
        print(f"  -> Rollback manifest: {result['rollback_manifest_path']}")

    # Markdown apply report
    if args.out:
        out_path = Path(args.out).resolve()
        out_path.parent.mkdir(parents=True, exist_ok=True)
        report = generate_apply_report(result)
