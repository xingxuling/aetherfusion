def _run_fusion_session(args: argparse.Namespace) -> None:
    """Execute the fusion-session subcommand."""
    source_path = Path(args.source).resolve()
    target_path = Path(args.target).resolve()
    modules = [m.strip() for m in args.modules.split(",") if m.strip()]
    reports_dir = Path(args.reports_dir).resolve()
    audit_path = args.audit_path or ""

    try:
        result = run_fusion_session(
            source_path=str(source_path),
            target_path=str(target_path),
            modules=modules,
            reports_dir=str(reports_dir),
            mode=args.mode,
            apply_confirm=args.apply_confirm,
            verify=args.verify,
            audit_path=audit_path,
        )
    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except RuntimeError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    # Print session summary
    module_results = result.get("module_results", {})
    total = len(modules)
    failed = sum(1 for v in module_results.values() if isinstance(v, dict) and v.get("status") == "failed")
    succeeded = total - failed

    print(f"\nFusion session complete: {result.get('session_id', '?')}")
    print(f"  Modules: {succeeded}/{total} succeeded")
    if failed:
        failed_names = [k for k, v in module_results.items() if isinstance(v, dict) and v.get("status") == "failed"]
        print(f"  Failed: {', '.join(failed_names)}")

    if result.get("verify_summary") and result["verify_summary"] is not None:
        vsum = result["verify_summary"]
        print(f"  Verify: {'PASSED' if vsum.get('passed') else 'FAILED'}")

    if result.get("diagnostic_plans", {}).get("generated"):
        plans = result["diagnostic_plans"]["generated"]
        print(f"  Diagnostic plans: {', '.join(plans)}")

    session_json_path = result.get("artifacts", {})
    print(f"  Session JSON: {reports_dir / 'fusion-session.json'}")
    print(f"  Session MD: {reports_dir / 'fusion-session.md'}")
    print(f"  Artifact index: {reports_dir / 'artifact-index.json'}")
    print(f"  Next action: {result.get('next_recommended_action', 'N/A')}")

    print("Done.")


